package com.example.moudle;

public class Them {
	public String appid;
	public int them;
	public Them(String appid, int them) {
		super();
		this.appid = appid;
		this.them = them;
	}
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public int getThem() {
		return them;
	}
	public void setThem(int them) {
		this.them = them;
	}
	
	
}
